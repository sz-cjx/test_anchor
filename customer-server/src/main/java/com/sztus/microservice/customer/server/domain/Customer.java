package com.sztus.microservice.customer.server.domain;

import com.sztus.framework.component.core.annotation.Column;
import com.sztus.framework.component.core.annotation.Entity;
import com.sztus.framework.component.core.annotation.Id;

@Entity
public class Customer {

    @Id
    @Column
    private Long id;

    @Column
    private String openId;

    @Column
    private String uniqueCode;

    @Column
    private Long createdAt;

    @Column
    private Long updatedAt;

    @Column
    private Integer status;

    @Column
    private String telephone;

    @Column
    private String email;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getOpenId() {
        return openId;
    }

    public void setOpenId(String openId) {
        this.openId = openId;
    }

    public String getUniqueCode() {
        return uniqueCode;
    }

    public void setUniqueCode(String uniqueCode) {
        this.uniqueCode = uniqueCode;
    }

    public Long getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(Long createdAt) {
        this.createdAt = createdAt;
    }

    public Long getUpdatedAt() {
        return updatedAt;
    }

    public void setUpdatedAt(Long updatedAt) {
        this.updatedAt = updatedAt;
    }

    public Integer getStatus() {
        return status;
    }

    public void setStatus(Integer status) {
        this.status = status;
    }

    public String getTelephone() {
        return telephone;
    }

    public void setTelephone(String telephone) {
        this.telephone = telephone;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}
